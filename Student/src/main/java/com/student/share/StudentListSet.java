package com.student.share;

import java.util.ArrayList;

public class StudentListSet {
	private int count = 0;
	private String msg = "";
	ArrayList<StudentList> list = new ArrayList<StudentList>();
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public ArrayList<StudentList> getList() {
		return list;
	}
	public void setList(ArrayList<StudentList> list) {
		this.list = list;
	}
	
	
			
}
